//        Java program to get the character at the given index within the String

public class String_1_GetCharacterAtGivenIndex {
    public static void main(String[] args) {
        String originalString = "Java Exercises!";
        System.out.println("The character at position 0 " + originalString.charAt(0));
        System.out.println("The character at position 10 " + originalString.charAt(10));

    }
}
